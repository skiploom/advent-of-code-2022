defmodule Day10Test do
  use ExUnit.Case, async: true
  alias AdventOfCode2022.Day10
  doctest Day10

  describe "solves the example puzzle" do
    test "part_one/0" do
      expected = 13140

      actual =
        String.split(input(), "\n", trim: true)
        |> Day10.parse_into_instructions()
        |> Day10.execute(0, &Day10.sum_signal/3)

      assert actual == expected
    end

    test "part_two/0" do
      expected =
        """
        ##..##..##..##..##..##..##..##..##..##..
        ###...###...###...###...###...###...###.
        ####....####....####....####....####....
        #####.....#####.....#####.....#####.....
        ######......######......######......####
        #######.......#######.......#######.....
        """
        |> String.split("\n", trim: true)

      actual =
        String.split(input(), "\n", trim: true)
        |> Day10.parse_into_instructions()
        |> Day10.execute([], &Day10.reverse_draw/3)
        |> Enum.reverse()
        |> Enum.chunk_every(40)
        |> Enum.map(&Enum.join/1)

      assert actual == expected
    end
  end

